import java.util.LinkedList;
import java.util.List;

/**
 * This class provides methods to create and manipulate a linked list of integers.
 * @author Obrie Scarbrough
 * @version 1.0
 * @since 6/1/2024
 */
public class ListOfLinks {
    public LinkedList<Integer> links;

    /**
     * Constructs a new ListOfLinks object with an empty list.
     * @since 6/1/24
     */
    public ListOfLinks() {
        this.links = links;
    }

    /**
     * Creates a linked list from the given list of elements.
     *@since 6/1/24
     * @param <T> The type of elements in the list.
     * @param List The list of elements to be added to the linked list.
     * @return The created linked list containing the elements from the input list.
     * Reuses the built-in linked list package to build the linked list.
     */
    public <T> List<Integer> CreateLinkedList(List<Integer> List) {
        links = (LinkedList<Integer>) new LinkedList<Integer>(List);
        return (java.util.List<Integer>) links;
    }
}





































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































